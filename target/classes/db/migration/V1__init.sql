CREATE TABLE tag (
  id BIGINT PRIMARY KEY AUTO_INCREMENT,
  name VARCHAR(64) NOT NULL,
  color VARCHAR(32) NOT NULL,
  created_at DATETIME(3) NOT NULL DEFAULT CURRENT_TIMESTAMP(3),
  updated_at DATETIME(3) NOT NULL DEFAULT CURRENT_TIMESTAMP(3) ON UPDATE CURRENT_TIMESTAMP(3),
  UNIQUE KEY uk_tag_name (name)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE card (
  id BIGINT PRIMARY KEY AUTO_INCREMENT,
  title VARCHAR(128) NOT NULL,
  created_at DATETIME(3) NOT NULL DEFAULT CURRENT_TIMESTAMP(3),
  updated_at DATETIME(3) NOT NULL DEFAULT CURRENT_TIMESTAMP(3) ON UPDATE CURRENT_TIMESTAMP(3)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE scene (
  id BIGINT PRIMARY KEY AUTO_INCREMENT,
  name VARCHAR(128) NOT NULL,
  icon VARCHAR(32) NOT NULL,
  pinned TINYINT(1) NOT NULL DEFAULT 0,
  created_at DATETIME(3) NOT NULL DEFAULT CURRENT_TIMESTAMP(3),
  updated_at DATETIME(3) NOT NULL DEFAULT CURRENT_TIMESTAMP(3) ON UPDATE CURRENT_TIMESTAMP(3)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE card_tag (
  card_id BIGINT NOT NULL,
  tag_id BIGINT NOT NULL,
  PRIMARY KEY (card_id, tag_id),
  KEY idx_card_tag_tag_id (tag_id),
  CONSTRAINT fk_card_tag_card FOREIGN KEY (card_id) REFERENCES card(id) ON DELETE CASCADE,
  CONSTRAINT fk_card_tag_tag FOREIGN KEY (tag_id) REFERENCES tag(id) ON DELETE RESTRICT
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE scene_tag (
  scene_id BIGINT NOT NULL,
  tag_id BIGINT NOT NULL,
  PRIMARY KEY (scene_id, tag_id),
  KEY idx_scene_tag_tag_id (tag_id),
  CONSTRAINT fk_scene_tag_scene FOREIGN KEY (scene_id) REFERENCES scene(id) ON DELETE CASCADE,
  CONSTRAINT fk_scene_tag_tag FOREIGN KEY (tag_id) REFERENCES tag(id) ON DELETE RESTRICT
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE scene_card_check (
  id BIGINT PRIMARY KEY AUTO_INCREMENT,
  user_id BIGINT NOT NULL DEFAULT 0,
  scene_id BIGINT NOT NULL,
  card_id BIGINT NOT NULL,
  checked TINYINT(1) NOT NULL DEFAULT 0,
  updated_at DATETIME(3) NOT NULL DEFAULT CURRENT_TIMESTAMP(3) ON UPDATE CURRENT_TIMESTAMP(3),
  UNIQUE KEY uk_scene_card_user (user_id, scene_id, card_id),
  KEY idx_scene_user (scene_id, user_id),
  KEY idx_card_user (card_id, user_id),
  CONSTRAINT fk_scene_card_check_scene FOREIGN KEY (scene_id) REFERENCES scene(id) ON DELETE CASCADE,
  CONSTRAINT fk_scene_card_check_card FOREIGN KEY (card_id) REFERENCES card(id) ON DELETE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;
